--For each Student and test, identify if their marks increased or decreased from the previous tests.

with prev_records as (
select  *,lag(marks) over(partition by studentid order by testid,subject,testdate asc) as prev_exam_marks 
from students
)
select studentid,studentname,subject,testid,testdate,marks,prev_exam_marks,
case when marks >= prev_exam_marks then 'INCREASED' else 'DECREASED' end as remarks
from prev_records;
